// Initialize score
let score = 0;

// Start the game by setting up the initial state and displaying players
function startGame() {
    document.getElementById("player1").style.display = "show";
    document.getElementById("player2").style.display = "show";
    comparePlayers(); // Indicates game start without choosing
}

// Get a random index for a player from the players array
function getRandomPlayerIndex() {
    return Math.floor(Math.random() * players.length);
}

// Compare two players based on their points
function comparePlayers(playerIndex) {
    const player1Index = getRandomPlayerIndex();
    let player2Index;

    do {
        player2Index = getRandomPlayerIndex();
    } while (player2Index === player1Index); // Ensure distinct players
    
    const player1 = players[player1Index];
    const player2 = players[player2Index];
    
    if (!player1.hasOwnProperty("Player") || !player2.hasOwnProperty("Player")) {
        alert("Error: One of the players is missing the Player property.");
        return;
    }
    
    if (!player1.hasOwnProperty("PTS") || !player2.hasOwnProperty("PTS")) {
        alert("Error: One of the players is missing the PTS property.");
        return;
    }
    
    // Display player names and total points
    document.getElementById("player1-name").textContent = player1.Player;
    document.getElementById("player2-name").textContent = player2.Player;
    document.getElementById("player1-points").textContent = `Total Points: ${player1.PTS}`;
    document.getElementById("player2-points").textContent = `Total Points: ${player2.PTS}`;
    
    if (playerIndex === 1) {
        if (player1.PTS > player2.PTS) {
            document.getElementById("result").textContent = "You chose correctly!";
            document.getElementById("result").style.color = "green";
            score++;
        } else {
            document.getElementById("result").textContent = "You chose incorrectly!";
            document.getElementById("result").style.color = " red";
            endGame();
        }
    } else if (playerIndex === 2) {
        if (player2.PTS > player1.PTS) {
            document.getElementById("result").textContent = "You chose correctly!";
            document.getElementById("result").style.color = "green";
            score++;
        } else {
            document.getElementById("result").textContent = "You chose incorrectly!";
            document.getElementById("result").style.color = " red";

            endGame();
        }
    } else {
        document.getElementById("result").textContent = "";
    }

    setTimeout(() => {
        document.getElementById("player1-points").style.display = "show";
        document.getElementById("player2-points").style.display = "show";
    }, 1000);
    
    document.getElementById("score").textContent = `Score: ${score}`;
}

